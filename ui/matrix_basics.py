"""
사이드바에 표시할 "행렬의 기본 개념" 정리 콘텐츠
"""

import streamlit as st


def render_matrix_basics():
    """
    NumPy·행렬 기초부터 이미지가 왜 숫자(행렬)로 다뤄지는지까지의
    개념을 사이드바에 접이식(expander) 카드 형태로 정리해서 보여준다.
    """
    st.sidebar.header("0. 행렬의 기본 개념")
    st.sidebar.caption("궁금한 항목을 눌러서 펼쳐보세요.")

    with st.sidebar.expander("📘 NumPy와 행렬이란?"):
        st.markdown(
            "- **NumPy**: 파이썬에서 행렬·배열을 빠르고 쉽게 계산하도록 "
            "도와주는 라이브러리\n"
            "- 일반 리스트는 한 줄(1차원)만 저장하지만, NumPy 배열은 여러 "
            "행과 열로 된 데이터(2차원 이상)를 저장 가능\n"
            "- 예: 1~9를 3×3 형태로 저장하면 아래처럼 하나의 행렬이 됨"
        )
        st.latex(r"\begin{bmatrix} 1 & 2 & 3 \\ 4 & 5 & 6 \\ 7 & 8 & 9 \end{bmatrix}")
        st.markdown("- 특정 값은 **(행 번호, 열 번호)** 로 접근한다 (예: 2행 3번째 값 = 6)")

    with st.sidebar.expander("➕➖ 덧셈과 뺄셈"):
        st.markdown(
            "- 같은 위치에 있는 숫자끼리 더하거나 뺀다\n"
            "- **조건**: 두 행렬의 크기가 완전히 같아야 한다\n"
            "  - ✅ 2×2 + 2×2 → 가능\n"
            "  - ❌ 2×2 + 3×3 → 불가능"
        )
        st.latex(
            r"\begin{bmatrix} 1 & 2 \\ 3 & 4 \end{bmatrix} + "
            r"\begin{bmatrix} 5 & 6 \\ 7 & 8 \end{bmatrix} = "
            r"\begin{bmatrix} 6 & 8 \\ 10 & 12 \end{bmatrix}"
        )

    with st.sidebar.expander("✖️ 곱셈 (조금 더 복잡함)"):
        st.markdown(
            "- **조건**: 앞 행렬의 **열 개수** = 뒤 행렬의 **행 개수** 여야 계산 가능\n"
            "  - 예: 3×2 행렬끼리는 그대로 곱할 수 없다 (앞 열 2 ≠ 뒤 행 3)\n"
            "- **순서가 중요하다** (교환법칙이 성립하지 않음)"
        )
        st.latex(r"A \times B \;\neq\; B \times A")
        st.markdown(
            "- 크기가 안 맞을 땐 **전치행렬**(행과 열을 서로 바꾼 행렬)을 이용해 "
            "곱셈이 가능하도록 만든다"
        )

    with st.sidebar.expander("🖼️ 이미지는 사실 숫자다"):
        st.markdown(
            "- 컴퓨터는 사진을 그림이 아니라 **숫자**로 저장한다\n"
            "- **흑백 이미지**: 각 위치(픽셀)에 '밝기 값' 하나만 저장\n"
            "- **컬러 이미지**: 각 위치에 R(빨강)·G(초록)·B(파랑) 세 값을 저장"
        )
        st.markdown(
            "| 벡터 [R, G, B] | 색상 |\n"
            "|---|---|\n"
            "| [1, 0, 0] | 빨강 |\n"
            "| [0, 1, 0] | 초록 |\n"
            "| [0, 0, 1] | 파랑 |\n"
            "| [1, 1, 1] | 흰색 |"
        )
        st.markdown("즉, 픽셀 하나도 사실은 숫자 3개로 표현된다.")

    with st.sidebar.expander("💡 밝기 조절 (곱셈 / 덧셈)"):
        st.markdown(
            "**곱셈으로 조절**\n"
            "- 모든 픽셀에 1보다 **작은** 수를 곱하면 → 어두워짐 (예: ×0.5 = 절반 밝기)\n"
            "- 1보다 **큰** 수를 곱하면 → 밝아짐\n\n"
            "**덧셈/뺄셈으로 조절**\n"
            "- 모든 픽셀에 일정한 숫자를 **더하면** → 밝아짐\n"
            "- 일정한 숫자를 **빼면** → 어두워짐\n"
            "- 곱셈 방식과 결과가 비슷해 보여도, 변화하는 방식 자체는 다르다"
        )

    with st.sidebar.expander("🎨 특정 색상만 바꾸기"):
        st.markdown(
            "- R(빨강) 채널 값만 키우면 → 이미지 전체가 붉게 보인다\n"
            "- G, B 채널도 각각 독립적으로 조절 가능\n"
            "- 사진 보정이나 필터 효과를 만들 때 자주 쓰이는 방식"
        )

    with st.sidebar.expander("🔄 반전과 변환"):
        st.markdown(
            "- **좌우반전**: 열(column) 순서를 뒤집는 것\n"
            "- **상하반전**: 행(row) 순서를 뒤집는 것\n"
            "- **색상 반전**: 밝은 부분 ↔ 어두운 부분을 서로 바꾸는 효과\n"
            "- **대각선 반전(전치)**: 행과 열을 서로 바꾸는 것 (전치행렬 개념)\n"
            "- 결국 모든 변환은 행렬 안 숫자의 **위치**를 바꾸는 작업이다"
        )

    with st.sidebar.expander("🔁 회전"):
        st.markdown(
            "- 90도, 180도 회전도 결국 행렬 안 숫자의 위치를 "
            "**규칙에 따라 재배열**하는 것이다\n"
            "- 화면에서는 그림이 돌아간 것처럼 보이지만, 실제로는 "
            "데이터의 위치가 바뀌는 것뿐이다"
        )

    st.sidebar.divider()